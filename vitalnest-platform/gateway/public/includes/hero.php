<!-- Doctor Animation Script -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    const heroDoctor = document.getElementById('heroDoctor');
    const heroDoor = document.getElementById('heroDoor');
    const medicalHubEnd = document.getElementById('medicalHubEnd');

    if (!heroDoctor || !heroDoor) return;

    // Function to trigger doctor animation
    function animateDoctor() {
        heroDoctor.classList.add('animating');

        // Add glow to Medical Hub when doctor is about to land (at ~60% of 4s = 2.4s)
        setTimeout(() => {
            if (medicalHubEnd) {
                medicalHubEnd.classList.add('doctor-landed');
            }
        }, 2400);

        // Remove animation class after it completes (4s)
        setTimeout(() => {
            heroDoctor.classList.remove('animating');
        }, 4000);

        // Remove glow from Medical Hub after doctor fades out (at 4.5s)
        setTimeout(() => {
            if (medicalHubEnd) {
                medicalHubEnd.classList.remove('doctor-landed');
            }
        }, 4500);
    }

    // Sync with door animation
    // Door opens at 15% of 8s = 1.2s, stays open until 70% = 5.6s
    // Initial delay of 3s before door animation starts
    // So first door open: 3s + 1.2s = 4.2s

    let animationInterval;

    function startDoctorCycle() {
        // First animation after door first opens
        setTimeout(() => {
            animateDoctor();

            // Then repeat every 8 seconds (matching door cycle)
            animationInterval = setInterval(() => {
                animateDoctor();
            }, 8000);
        }, 4200); // 3s delay + 1.2s into animation
    }

    startDoctorCycle();

    // Pause on hover
    const heroMainCircle = document.getElementById('heroMainCircle');
    if (heroMainCircle) {
        heroMainCircle.addEventListener('mouseenter', () => {
            clearInterval(animationInterval);
            heroDoctor.classList.remove('animating');
            // Also remove glow on pause
            if (medicalHubEnd) {
                medicalHubEnd.classList.remove('doctor-landed');
            }
        });

        heroMainCircle.addEventListener('mouseleave', () => {
            // Restart cycle when mouse leaves
            startDoctorCycle();
        });
    }
});
</script>
