<script>
    let currentSlide = 0;
    const totalSlides = 3;
    let slideInterval;

    function showSlide(index) {
        currentSlide = index;

        // Hide all slides
        document.querySelectorAll('#vision-mission .slide').forEach(slide => {
            slide.classList.remove('active');
            slide.classList.add('hidden');
        });

        // Show current slide
        const activeSlide = document.querySelector(`#vision-mission .slide[data-slide="${index}"]`);
        if (activeSlide) {
            activeSlide.classList.remove('hidden');
            activeSlide.classList.add('active');
        }

        // Update dots
        document.querySelectorAll('#vision-mission .slide-dot').forEach((dot, i) => {
            if (i === index) {
                dot.classList.add('active');
                dot.style.width = '1.5rem';
                dot.style.background = '#0F766E';
            } else {
                dot.classList.remove('active');
                dot.style.width = '0.625rem';
                dot.style.background = 'rgba(255,255,255,0.3)';
            }
        });

        // Reset auto-rotation timer
        resetInterval();
    }

    function nextSlide() {
        showSlide((currentSlide + 1) % totalSlides);
    }

    function prevSlide() {
        showSlide((currentSlide - 1 + totalSlides) % totalSlides);
    }

    function resetInterval() {
        clearInterval(slideInterval);
        slideInterval = setInterval(nextSlide, 6000);
    }

    // Start auto-rotation
    document.addEventListener('DOMContentLoaded', function() {
        slideInterval = setInterval(nextSlide, 6000);
    });
</script>

