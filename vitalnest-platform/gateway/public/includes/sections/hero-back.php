<script>
// Doctor emergence from Medical Hub and entry to Patient Home
document.addEventListener('DOMContentLoaded', function() {
    const hubCircle = document.getElementById('hubMainCircle');
    const homeCircle = document.getElementById('homeMainCircle');

    // Hub door functions
    function openHubDoors() {
        if (hubCircle) {
            hubCircle.classList.add('hub-doors-open');
        }
    }

    function closeHubDoors() {
        if (hubCircle) {
            hubCircle.classList.remove('hub-doors-open');
        }
    }

    // Home door functions
    function openHomeDoors() {
        if (homeCircle) {
            homeCircle.classList.add('home-doors-open');
        }
    }

    function closeHomeDoors() {
        if (homeCircle) {
            homeCircle.classList.remove('home-doors-open');
        }
    }

    // Animation cycle (15s total)
    function animationCycle() {
        // HUB: Doors open for doctor to emerge
        openHubDoors();
        setTimeout(closeHubDoors, 2000); // Close after 2 seconds

        // HOME: Doors open when doctor arrives (after ~13s of travel)
        setTimeout(openHomeDoors, 13000); // Open before doctor arrives
        setTimeout(closeHomeDoors, 15000); // Close after doctor enters (end of cycle)
    }

    // Run cycle every 15 seconds (match doctor animation)
    setInterval(animationCycle, 15000);

    // Initial cycle start
    setTimeout(animationCycle, 500);
});
</script>

